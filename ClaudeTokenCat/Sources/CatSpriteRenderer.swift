import AppKit

// MARK: - Cat Animation State

enum CatState: String, CaseIterable {
    case idle       // Sitting upright, tail wagging - before session
    case jumping    // Energetic jumping - low usage 0-40%
    case walking    // Calm walking - medium usage 40-80%
    case tired      // Lying down, yawning - very tired 80-99%
    case sleeping   // Sleeping with ZZ - depleted 100%
}

// MARK: - Pixel Art Sprite Renderer

/// Renders 20x20 pixel art cat sprites programmatically.
/// Each pixel is drawn as a filled rect in a bitmap context.
/// White pixels on transparent background, with `isTemplate = true`
/// so macOS automatically adapts to light/dark menu bar.
final class CatSpriteRenderer {

    static let spriteWidth = 28
    static let spriteHeight = 18
    static let scale: CGFloat = 2.0  // @2x for retina

    /// Cached rendered frames per state, built once on first access.
    private static var frameCache: [CatState: [NSImage]] = [:]

    /// Returns animation frames for a given cat state.
    static func frames(for state: CatState) -> [NSImage] {
        if let cached = frameCache[state] {
            return cached
        }
        let grids: [SpriteGrid]
        switch state {
        case .idle:
            grids = idleFrames
        case .jumping:
            grids = jumpingFrames
        case .walking:
            grids = walkingFrames
        case .tired:
            grids = tiredFrames
        case .sleeping:
            grids = sleepingFrames
        }
        let rendered = grids.map { renderSprite($0) }
        frameCache[state] = rendered
        return rendered
    }

    // MARK: - Rendering

    typealias SpriteGrid = [[UInt8]]

    private static func renderSprite(_ grid: SpriteGrid) -> NSImage {
        let px = Int(scale)
        let totalW = spriteWidth * px
        let totalH = spriteHeight * px
        let rep = NSBitmapImageRep(
            bitmapDataPlanes: nil,
            pixelsWide: totalW,
            pixelsHigh: totalH,
            bitsPerSample: 8,
            samplesPerPixel: 4,
            hasAlpha: true,
            isPlanar: false,
            colorSpaceName: .deviceRGB,
            bytesPerRow: 0,
            bitsPerPixel: 0
        )!

        NSGraphicsContext.saveGraphicsState()
        let ctx = NSGraphicsContext(bitmapImageRep: rep)!
        NSGraphicsContext.current = ctx

        // Clear to transparent
        NSColor.clear.setFill()
        NSRect(x: 0, y: 0, width: totalW, height: totalH).fill()

        // Draw white pixels
        NSColor.white.setFill()
        for row in 0..<spriteHeight {
            for col in 0..<spriteWidth {
                if row < grid.count && col < grid[row].count && grid[row][col] == 1 {
                    // Flip Y: row 0 is top of sprite, but NSRect y=0 is bottom
                    let flippedRow = spriteHeight - 1 - row
                    let rect = NSRect(
                        x: col * px,
                        y: flippedRow * px,
                        width: px,
                        height: px
                    )
                    rect.fill()
                }
            }
        }

        NSGraphicsContext.restoreGraphicsState()

        let image = NSImage(size: NSSize(width: CGFloat(spriteWidth), height: CGFloat(spriteHeight)))
        image.addRepresentation(rep)
        image.isTemplate = true
        return image
    }

